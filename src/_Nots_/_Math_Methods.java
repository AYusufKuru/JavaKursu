package _Nots_;

public class _Math_Methods {
    public static void main(String[] args) {

        int x = 0;
        int y = 0;

        Math.max(x,y);  //--Yüksek olan değişkeni verir.
        Math.min(x,y);  //--Düşük olan değişkeni verir.
        Math.sqrt(x);   //--Karekök verir.
        Math.abs(x);    //--Pozitif (Absolute) değer döndürür.
        Math.random();  //--0-1 aralığında rastgele bir sayı döndürür.

    }
}
